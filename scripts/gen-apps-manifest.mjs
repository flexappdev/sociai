#!/usr/bin/env node
// Scans ~/APPS/sociai/apps/*.jsx and regenerates:
//   src/lib/apps.ts                          (server-safe manifest)
//   src/app/apps/[slug]/AppRegistry.tsx      (client-side static-import registry)
// Runs via predev / prebuild hooks — drop a new .jsx in apps/, next dev|build
// picks it up automatically. Ported from ~/APPS/ms.

import { readFileSync, writeFileSync, readdirSync, existsSync, mkdirSync } from "node:fs";
import { fileURLToPath } from "node:url";
import { dirname, join, resolve } from "node:path";

const __dirname = dirname(fileURLToPath(import.meta.url));
const ROOT = resolve(__dirname, "..");
const APPS_DIR = join(ROOT, "apps");
const OUT_MANIFEST = join(ROOT, "src/lib/apps.ts");
const OUT_REGISTRY = join(ROOT, "src/app/apps/[slug]/AppRegistry.tsx");

function slugify(name) {
  return name
    .replace(/\.jsx$/i, "")
    .toLowerCase()
    .replace(/^the\s+/i, "")
    .replace(/[^a-z0-9]+/g, "-")
    .replace(/(^-+|-+$)/g, "");
}

function extractMeta(content) {
  const q = `["'\`]`;
  const pick = (key) => {
    const re = new RegExp(`${key}\\s*:\\s*${q}([^"'\`]+)${q}`);
    return content.match(re)?.[1];
  };
  return {
    title: pick("title"),
    subtitle: pick("subtitle"),
    brand: pick("brand"),
  };
}

function listJsxFiles() {
  if (!existsSync(APPS_DIR)) return [];
  return readdirSync(APPS_DIR)
    .filter((f) => f.endsWith(".jsx"))
    .sort();
}

function buildEntries() {
  return listJsxFiles().map((file, i) => {
    const filepath = join(APPS_DIR, file);
    const raw = readFileSync(filepath, "utf8");
    const meta = extractMeta(raw);
    const fallbackTitle = file.replace(/\.jsx$/i, "");
    return {
      idx: i,
      slug: slugify(file),
      file,
      title: meta.title || fallbackTitle,
      brand: meta.brand || "SociAI",
      blurb: meta.subtitle || "",
    };
  });
}

function esc(s) {
  return String(s).replace(/\\/g, "\\\\").replace(/"/g, '\\"');
}

function writeManifest(entries) {
  const lines = [
    "// AUTO-GENERATED by scripts/gen-apps-manifest.mjs — DO NOT EDIT.",
    "// Drop a new .jsx into ~/APPS/sociai/apps/ and rerun `npm run dev` or `npm run build`.",
    "",
    "export type AppEntry = {",
    "  slug: string;",
    "  title: string;",
    "  brand: string;",
    "  blurb: string;",
    "  file: string;",
    "};",
    "",
    "export const APPS: AppEntry[] = [",
    ...entries.map(
      (e) =>
        `  { slug: "${esc(e.slug)}", title: "${esc(e.title)}", brand: "${esc(e.brand)}", blurb: "${esc(e.blurb)}", file: "${esc(e.file)}" },`
    ),
    "];",
    "",
    "export function findAppBySlug(slug: string): AppEntry | undefined {",
    "  return APPS.find((a) => a.slug === slug);",
    "}",
    "",
  ];
  writeFileSync(OUT_MANIFEST, lines.join("\n"), "utf8");
}

function writeRegistry(entries) {
  mkdirSync(dirname(OUT_REGISTRY), { recursive: true });
  const importLines = entries.map(
    (e) => `import App${e.idx} from "../../../../apps/${e.file}";`
  );
  const registryLines = entries.map(
    (e) => `  "${esc(e.slug)}": App${e.idx},`
  );
  const lines = [
    "// AUTO-GENERATED by scripts/gen-apps-manifest.mjs — DO NOT EDIT.",
    '"use client";',
    "",
    'import type { ComponentType } from "react";',
    ...importLines,
    "",
    "export const REGISTRY: Record<string, ComponentType> = {",
    ...registryLines,
    "};",
    "",
  ];
  writeFileSync(OUT_REGISTRY, lines.join("\n"), "utf8");
}

const entries = buildEntries();
writeManifest(entries);
writeRegistry(entries);
console.log(
  `gen-apps-manifest: wrote ${entries.length} entr${entries.length === 1 ? "y" : "ies"} ` +
    `(${entries.map((e) => e.slug).join(", ") || "none"})`
);
